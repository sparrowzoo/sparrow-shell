package com.sparrow.param;

import com.sparrow.protocol.POJO;

public class ParamDemo implements POJO {
    private String name;
    private String age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
